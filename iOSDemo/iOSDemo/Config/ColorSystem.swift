import Foundation
import UIKit
struct ColorSystem {
    /// F7001E, red brand
    static let red: UIColor = UIColor.init(0xF7001E)
    /// FFE0E0, light red, error message background
    static let lightRed: UIColor = UIColor.init(0xFFE0E0)
    /// 333333, black
    static let black: UIColor = UIColor.init(0x333333)
    /// FFFFFF, white
    static let white: UIColor = UIColor.init(0xFFFFFF)
    /// F6F6F6, ash gray, main background
    static let ashGray: UIColor = UIColor.init(0xF6F6F6)
    /// EBEBEB, light gray, line, separator
    static let lightGray: UIColor = UIColor.init(0xEBEBEB)
    /// CBCBCB, gray
    static let gray: UIColor = UIColor.init(0xCBCBCB)
    /// 9C9C9C, dark gray
    static let darkGray: UIColor = UIColor.init(0x9C9C9C)
    /// 7ED321, green
    static let green: UIColor = UIColor.init(0x7ED321)
    /// F5A623, orange
    static let orange: UIColor = UIColor.init(0xF5A623)
    /// A975D4, purple
    static let purple: UIColor = UIColor.init(0xA975D4)
    /// 0897EE, blue
    static let blue: UIColor = UIColor.init(0x0897EE)
}
